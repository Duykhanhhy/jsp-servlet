package org.example.model;

public class Provinces {
    private int idProvince;
    private String nameProvince;
    private String note;

    //Getters, setters
    public int getIdProvince(){ return idProvince; }
    public void setIdProvince(int idProvince){ this.idProvince = idProvince; }
    public String getNameProvince(){ return nameProvince; }
    public void setNameProvince(String nameProvince){ this.nameProvince = nameProvince; }
    public String getNote(){ return note; }
    public void setNote(String note){ this.note = note; }
}
